    p.text = ""
    p = tf.add_paragraph()
    p.text = "Analysis:"
    p.font.bold = True
    p = tf.add_paragraph()
    p.text = "Lock-Free is ~2x faster than custom blocking. However, std::queue wins due to memory allocation chunking."

    # 7. Next Steps
    slide = prs.slides.add_slide(prs.slide_layouts[1])
    slide.shapes.title.text = "Next Phase: The Bounded Ring Buffer"
    tf = slide.shapes.placeholders[1].text_frame
    tf.text = "The Unbounded Lock-Free Queue exposed 'new node()' allocations as the ultimate bottleneck."
    p = tf.add_paragraph()
    p.text = "- The final evolution is our lockfree_spsc_bounded queue."
    p = tf.add_paragraph()
    p.text = "- Moves the memory to a compile-time Ring Buffer."
    p = tf.add_paragraph()
    p.text = "- Zero runtime allocations will obliterate all existing baseline benchmarks."
    
